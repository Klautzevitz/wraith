require 'io/console'

module Color
  RED = "\033[1;31m"
  GREEN = "\033[1;32m"
  YELLOW = "\033[1;33m"
  BLUE = "\033[1;34m"
  RESET = "\033[0m"

  def self.colorize(text, color)
    "#{color}#{text}#{RESET}"
  end
end

